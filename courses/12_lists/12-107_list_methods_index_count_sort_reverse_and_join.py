# index finding the position of a value in a list
nums = [6, 7, 8, 9, 10]
numbers.index(6) # 1
numbers.index(9) # 4

# count return the number of times x appears in the list
nums = [1, 1, 2, 3, 4, 5, 5]
nums.count(1) # 2

# reverse will reverse elements in the list (in-place)
nums = [1, 2, 3, 4]
nums.reverse()
print(nums) # [4, 3, 2, 1]

# sort will sort the item of the list
nums = [5, 2, 4, 1, 3]
nums.sort()
print(nums) # [1, 2, 3, 4, 5]

# join will combine every item in the list with the specified string
words = ['Coding', 'is', 'fun']
print(' '.join(words))
