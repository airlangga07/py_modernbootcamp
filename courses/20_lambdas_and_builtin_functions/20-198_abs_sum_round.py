# abs returns the absolute value of a numbero
abs(-4) # 4
abs(4) # 4

# sum takes an iterable and an optional start
# returns the sum of a start and the iters of an iterable from left to right
# and returns the total
sum([1, 2, 3, 4, 5]) # 15
sum([1, 2, 3, 4, 5], 10) # 25

# round will rounded to ndigits precision
round(10.2) # 10
round(1.21212121, 2) # 1.21
